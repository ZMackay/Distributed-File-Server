    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File file = chooser.getSelectedFile();
        Thread t = new UploadThread(file, null, clapi);
        t.start();
        JOptionPane.showMessageDialog(null, "Uploading file: " + file.getName());
        //Paint.startUpdate(jProgressBar1);
        try {
            t.join();
        } catch (InterruptedException ex) { 
            
        }
        //Paint.endUpdate();
        
        try {
            Thread.sleep(1000);
        } catch (InterruptedException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        refresh();
        JOptionPane.showMessageDialog(null, "File upload complete");
    }//GEN-LAST:event_jButton2ActionPerformed

    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String filename = tblFiles.getModel().getValueAt(tblFiles.getSelectedRow(),0).toString();
        //System.out.println(filename);
        
        Thread t = new DownloadThread(filename, null,clapi);
        t.start();
        JOptionPane.showMessageDialog(null, "Downloading file: " + filename);
        try {
            t.join();
            JOptionPane.showMessageDialog(null, "File download complete");
        } catch (InterruptedException ex) {
            JOptionPane.showMessageDialog(null, "file download failed");
        }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

