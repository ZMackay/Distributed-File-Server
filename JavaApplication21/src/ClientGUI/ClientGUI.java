    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String filename = tblFiles.getModel().getValueAt(tblFiles.getSelectedRow(),0).toString();
            //System.out.println(filename);
            //JOptionPane.showMessageDialog(null, "Deleting file: " + filename);
            Boolean worked = clapi.deleteFile(filename);
            
            Thread.sleep(1000);
            
            if(!worked)
                JOptionPane.showMessageDialog(null, "File deletion failed.");
            else {
                refresh();
                JOptionPane.showMessageDialog(null, "File deletion complete");
            }
            
        } catch (InterruptedException ex) {
            Logger.getLogger(ClientGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

