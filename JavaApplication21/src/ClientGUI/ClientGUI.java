    public void refresh(String a) {
        
        String[] files = clapi.getFilesArray();
        DefaultTableModel model = (DefaultTableModel) tblFiles.getModel();
        model.setRowCount(0);
        
        for(String i : files) {
            String[] j = i.split("::|::::|           |                      ");
            if(j.length < 3) {
                String[] k = new String[3];
                k[0] = j[0];
                k[1] = "Unkown";
                k[2] = j[1];
                j = k;
            }
            
            
            model.addRow(j);
        }
        
        tblFiles.setModel(model);
        
        
    }
    
